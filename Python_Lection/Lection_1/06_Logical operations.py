# обращение к элементу из списка массива
# + проверяем является ли число четное

list = [1,2,3,4]
is_odd = list[0] % 2 == 0 # 0 элемент в списки при делении с остатком на 2 даёт ноль?
print(is_odd)

# интересная особенность в python
# по умолчанию 0 это false а 1 это true

# то есть если мы напишем
is_odd = not list[0] % 2 # не является 0
print(is_odd)


