# Список list

# делаем копию

list_1 = [1, 2, 3]
list_2 = list_1 # делаем копию

list_1[0] = 123 # при таком изменении значение меняются в обоих списках
# вспоминаем что в pythone переменная это ссылка на хранящиеся данные
# меняем данные и по сути обе ссылки при таком копировании как выше
# ведут к одним и тем же записям

for e in list_1:
    print(e)

print()

for e in list_2:
    print(e)


print('===========')

