## удаление последнего элемента .pop()

list_11 = [1,2,3,4,5]

print(f'Длинна: {len(list_11)}')
print(list_11.pop())
print(list_11)
print('---')
print(f'Удаляем {list_11.pop()}')
print(list_11)

print('---')

##### удаление конкретного элемента
print(f'Удаляем {list_11.pop(0)}')
print(list_11)


